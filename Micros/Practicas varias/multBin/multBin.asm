	.cseg
	.org $0
	rjmp INICIO

INICIO: 
	LDI R16, LOW(RAMEND)
	OUT SPL, R16
	LDI R16, HIGH(RAMEND)
	OUT SPH, R16
	SER R16
	OUT DDRB, R16
	OUT PORTA, R16

LEE:
	IN 		R17, 	PINA
	MOV 	R18,	R17
	ANDI	R17,	$0F
	ANDI	R18,	$F0
	SWAP	R18
	MUL		R17,	R18
	MOVW	R18,	R0
	OUT		PORTB,	R18
	RJMP	LEE
